CREATE TABLE IF NOT EXISTS coupon_campaigns (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT UNSIGNED NOT NULL,
  store_id BIGINT UNSIGNED NOT NULL,
  name VARCHAR(100) NOT NULL,
  description VARCHAR(500) NOT NULL DEFAULT '',
  coupon_type VARCHAR(24) NOT NULL,
  threshold_cents BIGINT NOT NULL DEFAULT 0,
  discount_cents BIGINT NOT NULL,
  distribution_mode VARCHAR(24) NOT NULL DEFAULT 'PUBLIC_CLAIM',
  total_stock BIGINT UNSIGNED NOT NULL,
  issued_count BIGINT UNSIGNED NOT NULL DEFAULT 0,
  per_subject_limit INT UNSIGNED NOT NULL DEFAULT 1,
  claim_start_at DATETIME(3) NULL,
  claim_end_at DATETIME(3) NULL,
  validity_mode VARCHAR(24) NOT NULL DEFAULT 'RELATIVE_DAYS',
  valid_from DATETIME(3) NULL,
  valid_to DATETIME(3) NULL,
  valid_days INT UNSIGNED NOT NULL DEFAULT 30,
  order_types_json TEXT NOT NULL,
  status VARCHAR(24) NOT NULL DEFAULT 'DRAFT',
  version BIGINT UNSIGNED NOT NULL DEFAULT 1,
  created_by BIGINT UNSIGNED NOT NULL DEFAULT 0,
  updated_by BIGINT UNSIGNED NOT NULL DEFAULT 0,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  deleted_at DATETIME(3) NULL,
  PRIMARY KEY (id),
  UNIQUE KEY uk_coupon_campaigns_tenant_id (tenant_id, id),
  KEY idx_coupon_campaigns_store_status (tenant_id, store_id, status, claim_start_at, claim_end_at),
  CONSTRAINT fk_coupon_campaigns_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
  CONSTRAINT fk_coupon_campaigns_store FOREIGN KEY (store_id) REFERENCES stores(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS customer_coupons (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT UNSIGNED NOT NULL,
  store_id BIGINT UNSIGNED NOT NULL,
  campaign_id BIGINT UNSIGNED NOT NULL,
  coupon_no VARCHAR(48) NOT NULL,
  customer_id BIGINT UNSIGNED NULL,
  subject_key_hash CHAR(64) COLLATE utf8mb4_bin NOT NULL,
  source VARCHAR(24) NOT NULL,
  status VARCHAR(24) NOT NULL DEFAULT 'PROVISIONAL',
  campaign_snapshot_json TEXT NOT NULL,
  valid_from DATETIME(3) NOT NULL,
  valid_to DATETIME(3) NOT NULL,
  idempotency_key VARCHAR(128) COLLATE utf8mb4_bin NOT NULL,
  request_fingerprint CHAR(64) COLLATE utf8mb4_bin NOT NULL,
  claimed_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  used_at DATETIME(3) NULL,
  voided_at DATETIME(3) NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY uk_customer_coupons_no (tenant_id, coupon_no),
  UNIQUE KEY uk_customer_coupons_idempotency (tenant_id, idempotency_key),
  KEY idx_customer_coupons_campaign_subject (tenant_id, campaign_id, subject_key_hash, status),
  KEY idx_customer_coupons_customer (tenant_id, customer_id, status, valid_to),
  CONSTRAINT fk_customer_coupons_campaign FOREIGN KEY (tenant_id, campaign_id) REFERENCES coupon_campaigns(tenant_id, id),
  CONSTRAINT fk_customer_coupons_customer FOREIGN KEY (tenant_id, customer_id) REFERENCES customers(tenant_id, id),
  CONSTRAINT fk_customer_coupons_store FOREIGN KEY (store_id) REFERENCES stores(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

