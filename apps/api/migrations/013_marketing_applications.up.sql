  request_fingerprint CHAR(64) COLLATE utf8mb4_bin NOT NULL,
  occurred_at DATETIME(3) NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY uk_marketing_events_idempotency (tenant_id, idempotency_key),
  KEY idx_marketing_events_placement (tenant_id, placement_id, event_type, occurred_at),
  CONSTRAINT fk_marketing_events_placement FOREIGN KEY (tenant_id, placement_id) REFERENCES marketing_placements(tenant_id, id),
  CONSTRAINT fk_marketing_events_store FOREIGN KEY (store_id) REFERENCES stores(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

