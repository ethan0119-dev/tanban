CREATE TABLE IF NOT EXISTS lottery_prizes (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT UNSIGNED NOT NULL,
  campaign_id BIGINT UNSIGNED NOT NULL,
  name VARCHAR(100) NOT NULL,
  prize_type VARCHAR(24) NOT NULL,
  coupon_campaign_id BIGINT UNSIGNED NULL,
  weight BIGINT UNSIGNED NOT NULL,
  total_stock BIGINT UNSIGNED NOT NULL DEFAULT 0,
  awarded_count BIGINT UNSIGNED NOT NULL DEFAULT 0,
  sort_order INT NOT NULL DEFAULT 0,
  status VARCHAR(24) NOT NULL DEFAULT 'ACTIVE',
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY uk_lottery_prizes_tenant_id (tenant_id, id),
  KEY idx_lottery_prizes_campaign (tenant_id, campaign_id, status, sort_order),
  CONSTRAINT fk_lottery_prizes_campaign FOREIGN KEY (tenant_id, campaign_id) REFERENCES lottery_campaigns(tenant_id, id),
  CONSTRAINT fk_lottery_prizes_coupon FOREIGN KEY (tenant_id, coupon_campaign_id) REFERENCES coupon_campaigns(tenant_id, id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS lottery_draws (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT UNSIGNED NOT NULL,
  store_id BIGINT UNSIGNED NOT NULL,
  campaign_id BIGINT UNSIGNED NOT NULL,
  prize_id BIGINT UNSIGNED NULL,
  draw_no VARCHAR(48) NOT NULL,
  subject_key_hash CHAR(64) COLLATE utf8mb4_bin NOT NULL,
  business_date DATE NOT NULL,
  result_type VARCHAR(24) NOT NULL,
  result_reason VARCHAR(40) NOT NULL DEFAULT '',
  customer_coupon_id BIGINT UNSIGNED NULL,
  prize_snapshot_json TEXT NOT NULL,
  random_value BIGINT UNSIGNED NOT NULL,
  total_weight BIGINT UNSIGNED NOT NULL,
  idempotency_key VARCHAR(128) COLLATE utf8mb4_bin NOT NULL,
  request_fingerprint CHAR(64) COLLATE utf8mb4_bin NOT NULL,
  created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  PRIMARY KEY (id),
  UNIQUE KEY uk_lottery_draws_no (tenant_id, draw_no),
  UNIQUE KEY uk_lottery_draws_idempotency (tenant_id, idempotency_key),
  KEY idx_lottery_draws_subject_day (tenant_id, campaign_id, subject_key_hash, business_date),
  KEY idx_lottery_draws_campaign (tenant_id, campaign_id, created_at),
  CONSTRAINT fk_lottery_draws_campaign FOREIGN KEY (tenant_id, campaign_id) REFERENCES lottery_campaigns(tenant_id, id),
  CONSTRAINT fk_lottery_draws_prize FOREIGN KEY (tenant_id, prize_id) REFERENCES lottery_prizes(tenant_id, id),
  CONSTRAINT fk_lottery_draws_coupon FOREIGN KEY (customer_coupon_id) REFERENCES customer_coupons(id),
  CONSTRAINT fk_lottery_draws_store FOREIGN KEY (store_id) REFERENCES stores(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
